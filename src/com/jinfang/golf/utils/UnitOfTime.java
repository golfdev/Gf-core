package com.jinfang.golf.utils;

/**
 * 时间单位的常量类。
 *
 */
public class UnitOfTime {

	/**
	 * 秒/分钟。
	 */
	public static final int MILLS_1MINUTE = 60;// 1分钟

	/**
	 * 秒/小时。
	 */
	public static final int MILLS_1HOUR = 60 * MILLS_1MINUTE;// 1小时

	/**
	 * 秒/天。
	 */
	public static final int MILLS_1DAY = 86400;// 1天

	/**
	 * 秒/周。
	 */
	public static final int MILLS_1WEEK = 7 * MILLS_1DAY;// 1周

	/**
	 * 秒/月。
	 */
	public static final int MILLS_1MONTH = 30 * MILLS_1DAY;// 1月

}
